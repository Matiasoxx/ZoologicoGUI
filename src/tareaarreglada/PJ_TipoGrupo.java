    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        float sumaV;
        float sumaI;
        float sumaTotal;
        float cantidadGrupo;
        float porcentaje = 0;
        float porcentajeTipoVertebrado;
        float porcentajeTipoInvertebrado;
        String botonSeleccionado;
        CAnimal c = new CAnimal();        
        sumaV = c.obtenerCantidadFilas("mam√≠feros")+c.obtenerCantidadFilas("aves")+c.obtenerCantidadFilas("peces")+c.obtenerCantidadFilas("anfibios")+c.obtenerCantidadFilas("reptiles");
        sumaI = c.obtenerCantidadFilas("artr√≥podos")+c.obtenerCantidadFilas("moluscos")+c.obtenerCantidadFilas("equinodermos")+c.obtenerCantidadFilas("gusanos")+c.obtenerCantidadFilas("por√≠feros")+c.obtenerCantidadFilas("celent√©reos");
        //System.out.println("Mira la suma vertebrados : "+sumaV );
        //System.out.println("Mira la suma invertebrados : "+sumaI );
        sumaTotal = sumaI + sumaV;
        //System.out.println("Mira la suma total : "+sumaTotal );
        porcentajeTipoVertebrado = sumaV * 100/sumaTotal;//Sacamos
        //System.out.println("Mira el porcentaje vertebrado : "+porcentajeTipoVertebrado );
        porcentajeTipoInvertebrado = sumaI * 100/sumaTotal;
        //System.out.println("Mira el porcentaje invertebrado : "+porcentajeTipoInvertebrado );
        //Rescatamos el item seleccionado en el JComboBox1 
        String itemSeleccionado = jComboBox1.getSelectedItem().toString();
        //Preguntamos si el jRadioButton1 fue seleccionado de ser asi entramos aqui y en el caso contrario entramos al else
        if(jRadioButton1.isSelected()){
            botonSeleccionado = "Vertebrados";
            if(itemSeleccionado.equalsIgnoreCase("Mam√≠feros")) {//Si el item seleccionado es Mam√≠feros entramos aqu√≠ 
                cantidadGrupo = c.obtenerCantidadFilas("mam√≠feros");//Mandamos "mam√≠feros" a obtenerCantidadFilas para saber la cantidad de filas de la tabla mam√≠feros
                porcentaje = cantidadGrupo * 100 / sumaTotal;//Sacamos el porcentaje que sera el total de filas en la tabla mam√≠feros por 100 partido en la cantidad total de animales en el zoologico
            } else if(itemSeleccionado.equalsIgnoreCase("Aves")) {
                cantidadGrupo = c.obtenerCantidadFilas("aves");
                porcentaje = cantidadGrupo * 100 / sumaTotal;
            } else if(itemSeleccionado.equalsIgnoreCase("Peces")) {
                cantidadGrupo = c.obtenerCantidadFilas("peces");
                porcentaje = cantidadGrupo * 100 / sumaTotal;
            } else if(itemSeleccionado.equalsIgnoreCase("Anfibios")) {
                cantidadGrupo = c.obtenerCantidadFilas("anfibios");
                porcentaje = cantidadGrupo * 100 / sumaTotal;
            } else if(itemSeleccionado.equalsIgnoreCase("Reptiles")) {
                cantidadGrupo = c.obtenerCantidadFilas("reptiles");
                porcentaje = cantidadGrupo * 100 / sumaTotal;
            }
            Imposible1.setText(porcentajeTipoVertebrado + "%");//Al JTextField Imposible1 le pasamos el porcentaje del tipo Vertebrados (En este caso) 
            Imposible2.setText(porcentaje + "%");//Al JTextField Imposible2 le pasamos el porcentaje del grupo
        } 
        else if(jRadioButton2.isSelected()) {
            botonSeleccionado = "Invertebrados";
            if(itemSeleccionado.equalsIgnoreCase("Artr√≥podos")) {
                cantidadGrupo = c.obtenerCantidadFilas("artr√≥podos");
                porcentaje = cantidadGrupo * 100 / sumaTotal;
            } else if(itemSeleccionado.equalsIgnoreCase("Moluscos")) {
                cantidadGrupo = c.obtenerCantidadFilas("moluscos");
                porcentaje = cantidadGrupo * 100 / sumaTotal;
            } else if(itemSeleccionado.equalsIgnoreCase("Equinodermos")) {
                cantidadGrupo = c.obtenerCantidadFilas("equinodermos");
                porcentaje = cantidadGrupo * 100 / sumaTotal;
            } else if(itemSeleccionado.equalsIgnoreCase("Gusanos")) {
                cantidadGrupo = c.obtenerCantidadFilas("gusanos");
                porcentaje = cantidadGrupo * 100 / sumaTotal;
            } else if(itemSeleccionado.equalsIgnoreCase("Por√≠feros")) {
                cantidadGrupo = c.obtenerCantidadFilas("por√≠feros");
                porcentaje = cantidadGrupo * 100 / sumaTotal;
            } else if(itemSeleccionado.equalsIgnoreCase("Celent√©reos")) {
                cantidadGrupo = c.obtenerCantidadFilas("celent√©reos");
                porcentaje = cantidadGrupo * 100 / sumaTotal;
            }
            Imposible1.setText(porcentajeTipoInvertebrado + "%");//Al JTextField Imposible1 le pasamos el porcentaje del tipo Invertebrados (En este caso) 
            Imposible2.setText(porcentaje + "%");//Al JTextField Imposible2 le pasamos el porcentaje del grupo
        }
    }//GEN-LAST:event_jButton1ActionPerformed

